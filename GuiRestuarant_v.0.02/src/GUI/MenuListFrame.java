    private void AddToStockBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddToStockBtnActionPerformed
        if (selectedItemPanel == null) {
        JOptionPane.showMessageDialog(this, "Please select a menu item first.", "Selection Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        food selected = selectedItemPanel.getMenuItem();
        String type = selected.gettype();
        String stockFile;

        if (type.equalsIgnoreCase("Drinks")) {
            stockFile = "src\\menu\\RestaurantQueue\\DrinksStock.txt";
        } else if (type.equalsIgnoreCase("Dessert")) {
            stockFile = "src\\menu\\RestaurantQueue\\DessertStock.txt";
        } else if (type.equalsIgnoreCase("Maincourse")) {
            stockFile = "src\\menu\\RestaurantQueue\\MaincourseStock.txt";
        } else {
            JOptionPane.showMessageDialog(this, "Menu type not recognized. Cannot save to stock file.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String dataLine = String.format("%s,%s,%s,%.2f,%s",type,selected.getId(),selected.getName(),selected.getPrice(),selected.getImagePath()); 

        try (FileWriter fw = new FileWriter(stockFile, true);
             BufferedWriter bw = new BufferedWriter(fw);
             PrintWriter out = new PrintWriter(bw)) {

            out.println(dataLine); 

            JOptionPane.showMessageDialog(this,
                    String.format("Added '%s' to %s", selected.getName(), stockFile),
                    "Success", JOptionPane.INFORMATION_MESSAGE);

            selectedItemPanel.setSelected(false);
            selectedItemPanel = null;

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,
                    "Error writing to file " + stockFile + ": " + ex.getMessage(),
                    "File Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Error writing to stock file: " + stockFile, ex);
        }
    }//GEN-LAST:event_AddToStockBtnActionPerformed


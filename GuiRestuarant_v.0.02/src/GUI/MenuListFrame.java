    private void loadMenu(String fileName) {
        try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length == 5) {
                    String type = parts[0];
                    String id = parts[1];
                    String name = parts[2];
                    double price = Double.parseDouble(parts[3]);
                    String imagePath = parts[4];
                    
                    shop.Createmenu(type, id, name, price, imagePath);
                    food foodItem = shop.getAllmenu().get(shop.getAllmenu().size() - 1);

                    MenuItemPanel itemGUI = new MenuItemPanel(foodItem, this);
                    switch (type.toLowerCase()) {
                        case "maincourse":
                            MainPanel.add(itemGUI);
                            break;
                        case "drinks":
                            DrinkPanel.add(itemGUI);
                            break;
                        case "dessert":
                            DessertPanel.add(itemGUI);
                            break;
                    }        
                }
            }
            this.revalidate();
            this.repaint();

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, 
                "Error reading from file " + fileName + ": " + e.getMessage(), 
                "File Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Error reading menu file: " + fileName, e);
          } catch (InvalidOperationException ex) {
                Logger.getLogger(MainDisplay.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
                

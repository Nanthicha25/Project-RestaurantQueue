    private void displayFoodDetails() {
        if(menuItem != null) {
            TypeOutput.setText(menuItem.gettype());
            IdOutput.setText(menuItem.getId());
            NameOutput.setText(menuItem.getName());
            PriceOutput.setText(String.format("%.2f", menuItem.getPrice()));
            setImage(menuItem.getImagePath());
        }    
    }

    private void setImage(String imagePath) {
        if (imagePath == null || imagePath.isEmpty()) {
            ImageOutput.setText("No Image");
            return;
        }

        try {
            ImageIcon originalIcon = new ImageIcon(imagePath);
            Image img = originalIcon.getImage();
            
            final int DESIRED_SIZE = 280;

            Image scaledImg = img.getScaledInstance(
                DESIRED_SIZE,
                DESIRED_SIZE,
                Image.SCALE_SMOOTH 
            );
            
            // 4. ตั้งค่า ImageIcon ใหม่ที่ปรับขนาดแล้วให้กับ ImageOutput
            ImageOutput.setIcon(new ImageIcon(scaledImg));
            ImageOutput.setText(""); // ลบข้อความ "No Image" ออก
            
            ImageOutput.setPreferredSize(new java.awt.Dimension(DESIRED_SIZE, DESIRED_SIZE));
            
        } catch (Exception e) {
            // ถ้าโหลดรูปภาพไม่สำเร็จ (เช่น Path ผิด)
            ImageOutput.setIcon(null);
            ImageOutput.setText("Image Load Error: " + e.getMessage());
        }
    }
    

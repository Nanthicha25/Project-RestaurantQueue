    public void setSelected(boolean isSelected){
        isSelected = !isSelected;
        Color selected = new Color(153,153,255); 
        Color unselected = Color.white;
        Color tab; 
        if (isSelected) {
            tab = selected; 
        } else {
            tab = unselected;
        }
        this.setBackground(tab);
        jPanel1.setBackground(tab); 
        jPanel2.setBackground(tab); 
        jPanel3.setBackground(tab); 
        jPanel4.setBackground(tab); 
        jPanel5.setBackground(tab); 
        jPanel6.setBackground(tab); 
        jPanel7.setBackground(tab); 
  
        this.revalidate();
        this.repaint();
        
    } 

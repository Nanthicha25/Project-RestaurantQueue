private void saveRowToFile(String id, String name, String price) {
   try (FileWriter fw = new FileWriter("File\\Drink.csv", true)) { // true = append
        fw.write(id + "," + name + "," + price + "\n");
    } catch (IOException e) {
        e.printStackTrace();
    }
    try (FileWriter fw = new FileWriter("File\\Dessesert.csv", true)) { // true = append
        fw.write(id + "," + name + "," + price + "\n");
    } catch (IOException e) {
        e.printStackTrace();
    }
    try (FileWriter fw = new FileWriter("File\\MainCourse.csv", true)) { // true = append
        fw.write(id + "," + name + "," + price + "\n");
    } catch (IOException e) {
        e.printStackTrace();
    }
}
private void saveTableToFile() {
    try (FileWriter fw = new FileWriter("File\\Drink.csv")) { // ไม่ใส่ true → เขียนทับทั้งไฟล์
        DefaultTableModel model = (DefaultTableModel) DrinkTable.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            fw.write(model.getValueAt(i, 0) + "," 
                   + model.getValueAt(i, 1) + "," 
                   + model.getValueAt(i, 2) + "\n");
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    try (FileWriter fw = new FileWriter("File\\Dessesert.csv")) { // ไม่ใส่ true → เขียนทับทั้งไฟล์
        DefaultTableModel model = (DefaultTableModel) DessertTable.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            fw.write(model.getValueAt(i, 0) + "," 
                   + model.getValueAt(i, 1) + "," 
                   + model.getValueAt(i, 2) + "\n");
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    try (FileWriter fw = new FileWriter("File\\MainCourse.csv")) { // ไม่ใส่ true → เขียนทับทั้งไฟล์
        DefaultTableModel model = (DefaultTableModel) MainTable.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            fw.write(model.getValueAt(i, 0) + "," 
                   + model.getValueAt(i, 1) + "," 
                   + model.getValueAt(i, 2) + "\n");
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}

                                      

